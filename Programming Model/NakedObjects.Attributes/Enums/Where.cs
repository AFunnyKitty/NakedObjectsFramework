namespace NakedObjects {
    /// <summary>
    ///     Enum to indicate location of behaviour
    /// </summary>
    public enum Where {
        /// <summary>
        ///     Behaviour occurs at default location
        /// </summary>
        Default,

        /// <summary>
        ///     Behaviour occurs at local location
        /// </summary>
        Locally,

        /// <summary>
        ///     Behaviour occurs at remote location
        /// </summary>
        Remotely
    }
}